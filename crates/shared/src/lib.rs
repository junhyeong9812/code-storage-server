// =============================================================================
// shared 크레이트 - 진입점 (lib.rs)
// =============================================================================
//
// lib.rs는 라이브러리 크레이트의 루트 파일
// - 여기서 pub mod로 선언한 모듈이 외부에 공개됨
// - 다른 크레이트에서 use shared::error::AppError; 처럼 사용
//
// 파일 위치: crates/shared/src/lib.rs
//
// 사용 예시 (다른 크레이트에서):
//   use shared::error::AppError;
//   use shared::types::{Id, Timestamp, new_id, now};
// =============================================================================

// -----------------------------------------------------------------------------
// 모듈 선언
// -----------------------------------------------------------------------------
// pub mod <이름>: 해당 이름의 모듈을 공개로 선언
//
// Rust 모듈 시스템:
// - mod error; → src/error.rs 또는 src/error/mod.rs 파일을 모듈로 로드
// - pub mod → 외부 크레이트에서 접근 가능
// - mod (pub 없음) → 이 크레이트 내부에서만 접근 가능

